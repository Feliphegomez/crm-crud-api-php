						La Empresa ha dispuesto al servicio de sus usuarios y de los diferentes grupos de interés de la institución, una alternativa para facilitar la radicación de PQRSF que comprenden asuntos como Peticiones, Quejas, Reclamos, Sugerencias y Felicitaciones, a través de una plataforma que le permitirá registrar cualquier tipo de requerimiento y efectuar su seguimiento.
					</p>
					<p>
						Para facilitar el registro de sus PQRSF, a continuación describimos cada una de ellas:
						<br>
						<ul>
							<li>
								<b>Derecho de Petición o Petición:</b>
								Se entiende por Derecho de Petición, la solicitud respetuosa que hacen las personas naturales o jurídicas ante las autoridades o Entidades Públicas o privadas. La Constitución Política establece, en su artículo 23, el derecho de petición.
							</li>
							<li>
								<b>Queja:</b>
								Se entiende por queja la inconformidad manifestada por un usuario frente a la actuación de un funcionario o trabajador, por causa o con ocasión del ejercicio irregular de sus funciones.
							</li>
							<li>
								<b>Reclamo:</b>
								Es la solicitud de investigación por una irregularidad cometida por alguno de los actores o por el incumplimiento de las obligaciones establecidas en la Ley y sus normas complementarias, y las que la modifica.
							</li>
							<li>
								<b>Sugerencia:</b>
								Es una propuesta que se presenta para incidir o mejorar un proceso cuyo objeto está relacionado con la prestación de un servicio o el cumplimiento de una función pública.
							</li>
							<li>
								<b>Felicitación:</b>
								Es la manifestación que expresa el agrado o satisfacción con un funcionario o con el proceso que genera el servicio.
							</li>
